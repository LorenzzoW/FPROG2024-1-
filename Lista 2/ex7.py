#Quantidade de gramas consumidas por dia pelos pássaros
quantidade_de_gramas = 30

#Quantidade de pássaros
quantidade_de_passaros = int(input("Quantidade de pássaros que o criador de pássaros tem:"))

#Total de ração necessária
total_necessário = quantidade_de_gramas * quantidade_de_passaros

#Resultado
print(f"A quantidade de ração necessária por dia para os pássaros é {total_necessário}")